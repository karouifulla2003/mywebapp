// Modelos para el sistema de administración (MongoDB)
import mongoose from "mongoose";

const userSchema = new mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
      unique: true,
      min: 3,
      max: 20,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
    img: {
      type: String,
    },
    isAdmin: {
      type: Boolean,
      default: false,
    },
    isActive: {
      type: Boolean,
      default: true,
    },
    phone: {
      type: String,
    },
    address: {
      type: String,
    },
  },
  { timestamps: true }
);

const productSchema = new mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
      unique: true,
    },
    desc: {
      type: String,
      required: true,
    },
    price: {
      type: Number,
      required: true,
      min: 0,
    },
    stock: {
      type: Number,
      required: true,
      min: 0,
    },
    img: {
      type: String,
    },
    color: {
      type: String,
    },
    size: {
      type: String,
    },
  },
  { timestamps: true }
);

// Exportamos con un prefijo 'Admin' para evitar conflictos con posibles modelos similares
// en el proyecto principal
export const AdminUser = mongoose.models.User || mongoose.model("User", userSchema);
export const AdminProduct = mongoose.models.Product || mongoose.model("Product", productSchema);

// Exportamos también con nombres originales para mantener compatibilidad con código existente
export const User = AdminUser;
export const Product = AdminProduct;